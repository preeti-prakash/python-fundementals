#built-in functions

print("Hello")  #prints hello


#abs() - returns absolute value of the given number
print( abs(10.5) )  # prints 10.5
 
print( abs(-10.5) )  # prints 10.5

#pow() function returns the value of x to the power of y.

pow(2, 5)  # returns: 32
pow(10, 3)  # returns: 1000

# max() and min() return the largest and smallest of the input values, respectively.
max(34, 45, 67)  # returns: 67
 
min(34, 45, 67)  # returns: 34

