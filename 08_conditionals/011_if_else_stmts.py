# If, Else, and Elif in Python

i = 2
if i%2 == 0:
    print("i is even");
    
# The else statement provides an alternative code block that will execute if the if statement's condition is not met

i = 3
if i%2 == 0:
    print("i is even");
else:
    print("i is odd");
    
# elif stands for "else if". This statement allows us to check multiple conditions.
i = 3
if i==1:
    print("i is 1")
elif i==2:
    print("i is 2")
else:
    print("i is not 1 or 2")
    
