i = 5  # Assigns the value 5 to the variable i


# Assignment of one variable's value to another variable
j = i  # Assigns the value of i to j
print(j)  # Outputs: 5

# Using expressions in assignments
j = 2 * i  # Assigns the value of the expression (2*i) to j
print(j)  # Outputs: 10


# The = symbol in programming versus mathematics
# 5 = j  # Raises a SyntaxError


# Assignments involving operations
num1 = 5
num2 = 3
sum = num1 + num2  # Adds num1 and num2 and assigns the result to sum
print(sum)  # Outputs: 8

# Multiple assignments and operations
a = 5
b = 6
c = 7
sum = a + b + c  # Adds a, b and c and assigns the result to sum
print(sum)  # Outputs: 18

# Updating a variable's value
index = 0
index = index + 1  # Adds 1 to the current value of index and updates it
print(index)  # Outputs: 1